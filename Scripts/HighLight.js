function onmouseoutColor1(source) {
    source.style.backgroundColor = '#EFF3FB';
    source.style.color = 'black';
}

function onmouseoverColor1(source) {
    source.style.backgroundColor = '#c0c0ff';
    source.style.color = 'white';
}

